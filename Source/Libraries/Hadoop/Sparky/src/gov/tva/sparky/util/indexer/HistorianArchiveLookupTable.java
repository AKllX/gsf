	public String Lookup_Path_ByFileID( int iFileID ) throws Exception {
		
		String strFwdRowKey_FileID = CalculateRowKeyString( iFileID );
		
		return Lookup_Path_ByFileID( strFwdRowKey_FileID );
		
	}
	
	// forward lookup
	public String Lookup_Path_ByFileID( String strFileID ) throws Exception {
	
		// check for it locally
		
		if ( this.oCached_Forward_LookupTable.containsKey( strFileID ) ) {
			
			
		} else {
			
			// pull it from storage, cache it, return it
			
			HistorianArchiveLookupTableEntry oFwdEntry = ForwardLookupEntry_ByHDFSFileID( strFileID );
			
			// if we cant find it in storage, throw exception
			
			if ( null == oFwdEntry ) {
				
				throw new Exception( "Could not find the hdfs path specified by key: " + strFileID );
				
			}
			
			// update forward table
			this.oCached_Forward_LookupTable.put(strFileID, oFwdEntry.GetHdfsPath());
			
			// update reverse table
			this.oCached_Reverse_LookupTable.put(oFwdEntry.GetHdfsPath(), strFileID);
			
		}
		
		return this.oCached_Forward_LookupTable.get(strFileID);
		
	}
	
	public String Lookup_FileID_byPath( String strHdfsPath ) throws Exception {
	
		// check for it locally
		
		if ( this.oCached_Reverse_LookupTable.containsKey( strHdfsPath ) ) {
			
			
		} else {
			
			// pull it from storage, cache it, return it
			
			HistorianArchiveLookupTableEntry oRevEntry = ReverseLookupEntry_ByPath( strHdfsPath ); //ForwardLookupEntry_ByHDFSFileID( strFileID );
			
			// if we cant find it in storage, throw exception
			
			if ( null == oRevEntry ) {
				
				throw new Exception( "Could not find the hdfs path specified by path: " + strHdfsPath );
				
			}
			
			// update forward table
			this.oCached_Forward_LookupTable.put(oRevEntry.GetForwardKey(), oRevEntry.GetHdfsPath());
			
			// update reverse table
			this.oCached_Reverse_LookupTable.put(oRevEntry.GetHdfsPath(), oRevEntry.GetForwardKey());
			
		}
		
		return this.oCached_Reverse_LookupTable.get(strHdfsPath);
		
	}

	public static String CalculateRowKeyString( int iRowCount ) {
		return String.format( "%012d", iRowCount );
	}	
	
	public static int RetrieveAndIncArchiveRowCount() {

		int iRowCount = RetrieveFileArchiveRowCount();

		iRowCount++;
		
		// now update the table
		
		byte[] arBytes = HDFSPointBlockPointer.intToByteArray(iRowCount);
		
		try {
			RestProxy.InsertHbaseIndexBucket( HBASE_HDFSARCHIVELOOKUPTABLE_TABLE_NAME, HBASE_HDFSARCHIVELOOKUPTABLE_TRACKING_ROWCOUNT_KEY, HBASE_HDFSARCHIVELOOKUPTABLE_TRACKING_COL_NAME, HBASE_HDFSARCHIVELOOKUPTABLE_TRACKING_QUAL_NAME , arBytes);
		} catch (URIException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		
		return iRowCount;
		
	}
	
	public static int RetrieveFileArchiveRowCount() {

		int iRowCount = 0;
		
		byte[] arCountBytes = null;
		
		try {
			
			arCountBytes = RestProxy.QueryHBaseForCell( HBASE_HDFSARCHIVELOOKUPTABLE_TABLE_NAME, HBASE_HDFSARCHIVELOOKUPTABLE_TRACKING_ROWCOUNT_KEY, HBASE_HDFSARCHIVELOOKUPTABLE_TRACKING_COL_NAME, HBASE_HDFSARCHIVELOOKUPTABLE_TRACKING_QUAL_NAME );
			
		} catch (ParserConfigurationException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (SAXException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		
		if ( null == arCountBytes ) {
			
		} else {
			iRowCount = HDFSPointBlockPointer.byteArrayToInt( arCountBytes );
		}
		
		if ( iRowCount < 0  ) {
			iRowCount = 0;
		}
		
		return iRowCount;
		
	}	


