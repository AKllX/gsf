	public static boolean Delete_HBase_LookupTableEntries( String strHdfsPath ) {

		HistorianArchiveLookupTable table = new HistorianArchiveLookupTable();
		HistorianArchiveLookupTableEntry entry = new HistorianArchiveLookupTableEntry();		
		entry.SetHdfsPath( strHdfsPath );

		String strFileID_Ret = "";
		
		try {
			
			strFileID_Ret = table.Lookup_FileID_byPath(strHdfsPath);
		
		} catch (Exception e) {

			strFileID_Ret = ""; // archive does not exist in lookup table
			
		}
		
		if ( strFileID_Ret.equals( "" ) ) {
			
			System.out.println( "Archive does not exist in reverse lookup table" );
			return false;
			
		}				
		
		try {
			RestProxy.DeleteHbaseRow( HBASE_HDFSARCHIVE_REVERSE_LOOKUPTABLE_TABLE_NAME, entry.GetReverseKey() );
		} catch (URIException e) {
			// TODO Auto-generated catch block
			System.out.println( "Path did not exist in HBase reverse lookup table: " + strHdfsPath );
			return false;
		}
		
		// now hit the forward table
			
		try {
			RestProxy.DeleteHbaseRow( HBASE_HDFSARCHIVELOOKUPTABLE_TABLE_NAME, strFileID_Ret );
		} catch (URIException e) {
			// TODO Auto-generated catch block
			System.out.println( "Path did not exist in HBase forward lookup table: " + strHdfsPath );
			return false;
		}		
		
		return true;
		
	}
	
