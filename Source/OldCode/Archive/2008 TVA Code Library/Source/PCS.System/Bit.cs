namespace System
{
    /// <summary>
    /// Bit constants defined for bits 0 to 31.
    /// </summary>
    [CLSCompliant(false)]
    public static class Bit
    {
        /// <summary>No bits set (0x00000000)</summary>
        public const byte Nil = 0x0;

        // Byte 0, Bits 0-7

        /// <summary>Bit 0 (0x00000001)</summary>
        public const byte _00 = 0x00000001;    // 00000001 = 1

        /// <summary>Bit 1 (0x00000002)</summary>
        public const byte _01 = 0x00000002;    // 00000010 = 2

        /// <summary>Bit 2 (0x00000004)</summary>
        public const byte _02 = 0x00000004;    // 00000100 = 4

        /// <summary>Bit 3 (0x00000008)</summary>
        public const byte _03 = 0x00000008;    // 00001000 = 8

        /// <summary>Bit 4 (0x00000010)</summary>
        public const byte _04 = 0x00000010;    // 00010000 = 16

        /// <summary>Bit 6 (0x00000020)</summary>
        public const byte _05 = 0x00000020;    // 00100000 = 32

        /// <summary>Bit 6 (0x00000040)</summary>
        public const byte _06 = 0x00000040;    // 01000000 = 64

        /// <summary>Bit 7 (0x00000080)</summary>
        public const byte _07 = 0x00000080;    // 10000000 = 128

        // Byte 1, Bits 8-15

        /// <summary>Bit 8 (0x00000100)</summary>
        public const ushort _08 = 0x00000100;

        /// <summary>Bit 9 (0x00000200)</summary>
        public const ushort _09 = 0x00000200;

        /// <summary>Bit 10 (0x00000400)</summary>
        public const ushort _10 = 0x00000400;

        /// <summary>Bit 11 (0x00000800)</summary>
        public const ushort _11 = 0x00000800;

        /// <summary>Bit 12 (0x00001000)</summary>
        public const ushort _12 = 0x00001000;

        /// <summary>Bit 13 (0x00002000)</summary>
        public const ushort _13 = 0x00002000;

        /// <summary>Bit 14 (0x00004000)</summary>
        public const ushort _14 = 0x00004000;

        /// <summary>Bit 15 (0x00008000)</summary>
        public const ushort _15 = 0x00008000;

        // Byte 2, Bits 16-23

        /// <summary>Bit 16 (0x00010000)</summary>
        public const uint _16 = 0x00010000;

        /// <summary>Bit 17 (0x00020000)</summary>
        public const uint _17 = 0x00020000;

        /// <summary>Bit 18 (0x00040000)</summary>
        public const uint _18 = 0x00040000;

        /// <summary>Bit 19 (0x00080000)</summary>
        public const uint _19 = 0x00080000;

        /// <summary>Bit 20 (0x00100000)</summary>
        public const uint _20 = 0x00100000;

        /// <summary>Bit 21 (0x00200000)</summary>
        public const uint _21 = 0x00200000;

        /// <summary>Bit 22 (0x00400000)</summary>
        public const uint _22 = 0x00400000;

        /// <summary>Bit 23 (0x00800000)</summary>
        public const uint _23 = 0x00800000;

        // Byte 3, Bits 24-31

        /// <summary>Bit 24 (0x01000000)</summary>
        public const uint _24 = 0x01000000;

        /// <summary>Bit 25 (0x02000000)</summary>
        public const uint _25 = 0x02000000;

        /// <summary>Bit 26 (0x04000000)</summary>
        public const uint _26 = 0x04000000;

        /// <summary>Bit 27 (0x08000000)</summary>
        public const uint _27 = 0x08000000;

        /// <summary>Bit 28 (0x10000000)</summary>
        public const uint _28 = 0x10000000;

        /// <summary>Bit 29 (0x20000000)</summary>
        public const uint _29 = 0x20000000;

        /// <summary>Bit 30 (0x40000000)</summary>
        public const uint _30 = 0x40000000;

        /// <summary>Bit 31 (0x80000000)</summary>
        public const uint _31 = 0x80000000;
    }
}