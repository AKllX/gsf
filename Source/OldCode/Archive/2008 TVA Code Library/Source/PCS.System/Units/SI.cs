namespace System
{
    /// <summary>
    /// Defines constant factors for SI prefix (i.e., metric) conversions.
    /// </summary>
    public static class SI
    {
        /// <summary>
        /// SI prefix Y, 10^24
        /// </summary>
        public const double yotta = 1.0e+24D;

        /// <summary>
        /// SI prefix Z, 10^21
        /// </summary>
        public const double zetta = 1.0e+21D;

        /// <summary>
        /// SI prefix E, 10^18
        /// </summary>
        public const double exa = 1.0e+18D;

        /// <summary>
        /// SI prefix P, 10^15
        /// </summary>
        public const double peta = 1.0e+15D;

        /// <summary>
        /// SI prefix T, 10^12
        /// </summary>
        public const double tera = 1.0e+12D;

        /// <summary>
        /// SI prefix G, 10^9
        /// </summary>
        public const double giga = 1.0e+9D;

        /// <summary>
        /// SI prefix M, 10^6
        /// </summary>
        public const double mega = 1.0e+6D;

        /// <summary>
        /// SI prefix k, 10^3
        /// </summary>
        public const double kilo = 1.0e+3D;

        /// <summary>
        /// SI prefix h, 10^2
        /// </summary>
        public const double hecto = 1.0e+2D;

        /// <summary>
        /// SI prefix da, 10^1
        /// </summary>
        public const double deca = 1.0e+1D;

        /// <summary>
        /// SI prefix d, 10^-1
        /// </summary>
        public const double deci = 1.0e-1D;

        /// <summary>
        /// SI prefix c, 10^-2
        /// </summary>
        public const double centi = 1.0e-2D;

        /// <summary>
        /// SI prefix m, 10^-3
        /// </summary>
        public const double milli = 1.0e-3D;

        /// <summary>
        /// SI prefix µ, 10^-6
        /// </summary>
        public const double micro = 1.0e-6D;

        /// <summary>
        /// SI prefix n, 10^-9
        /// </summary>
        public const double nano = 1.0e-9D;

        /// <summary>
        /// SI prefix p, 10^-12
        /// </summary>
        public const double pico = 1.0e-12D;

        /// <summary>
        /// SI prefix f, 10^-15
        /// </summary>
        public const double femto = 1.0e-15D;

        /// <summary>
        /// SI prefix a, 10^-18
        /// </summary>
        public const double atto = 1.0e-18D;

        /// <summary>
        /// SI prefix z, 10^-21
        /// </summary>
        public const double zepto = 1.0e-21D;

        /// <summary>
        /// SI prefix y, 10^-24
        /// </summary>
        public const double yocto = 1.0e-24D;
    }
}
